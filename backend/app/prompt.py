"""Prompt builder — ประกอบ system prompt + user message ก่อนยิง LLM (spec §5, §6).

system prompt สร้างจาก RULES registry (rules.py) เพื่อให้ checklist กฎกับตัวคัดกรอง
การอ้างอิง (filter_findings) ใช้รหัสกฎชุดเดียวกัน ไม่หลุดออกจากกัน.

เฟสนี้ใช้ few-shot: ฝังตัวบทกฎที่อ้างจริง (reference.py) + ตัวอย่างคู่ผิด/เฉลย
จากโฟลเดอร์ 03 (fewshot.py) เข้า system prompt (architecture.md — ตรวจด้วย AI รอบเดียว).
"""

from __future__ import annotations

from app.extract import CheckContext
from app.fewshot import build_fewshot_block
from app.reference import LEGAL_PROVISIONS
from app.rules import RULES


def build_system_prompt() -> str:
    """system prompt: บทบาท + ประเภทข้อผิด + ตัวบทกฎ + checklist 15 กฎ + กติกา + ตัวอย่าง few-shot."""
    rules_block = "\n".join(
        f"  {r.rule_id} [{r.check_type}] {r.description} (อ้างอิง: {r.source})"
        for r in RULES
    )
    fewshot_block = build_fewshot_block()
    fewshot_section = (
        f"\n\nตัวอย่างการตรวจ (เรียนรู้จากคู่ TOR ที่มีข้อผิดและผลตรวจที่ถูกต้อง):\n{fewshot_block}"
        if fewshot_block
        else ""
    )
    return f"""คุณเป็นผู้ตรวจเอกสาร TOR งานจัดซื้อจัดจ้างภาครัฐของไทย
หน้าที่คือตรวจหาจุดที่เขียนผิดระเบียบ เขียนไม่ครบ หรือเขียนขัดแย้งกันเอง
แล้วรายงานเป็นรายการข้อค้นพบ (findings) ขอบเขตเฟสนี้คืองานจ้างทั่วไป แบบเต็ม

ตรวจข้อผิด 4 ประเภท (error_class):
  1 = โครงสร้าง/ความครบถ้วน เช่น หัวข้อบังคับครบไหม เลขข้อต่อเนื่องไหม
  2 = ค่าที่เขียนขัดระเบียบ เช่น อัตราค่าปรับนอกกรอบ ระบุยี่ห้อ เกณฑ์แคบ
  3 = ความสอดคล้องภายในเอกสาร เช่น งวดงานกับงวดเงิน วันส่งมอบนอกช่วงโครงการ
  4 = ความสมเหตุสมผลของเนื้อหา เช่น ความเป็นมากับวัตถุประสงค์สัมพันธ์กันไหม

ตัวบทกฎหมายอ้างอิง (ใช้เป็นเกณฑ์ตัดสิน):
{LEGAL_PROVISIONS}

checklist กฎที่ตรวจได้ (ใช้ rule_id เหล่านี้เท่านั้น ห้ามแต่งรหัสใหม่):
{rules_block}

กติกาสำคัญ:
- ข้อ 1 ถึง 9 ตาม ว 159 เป็นหัวข้อบังคับขั้นต่ำ ข้อ 10 (การรับประกันความชำรุดบกพร่อง)
  เป็นทางเลือก ถ้าไม่มีข้อ 10 อย่ารายงานว่าผิด หัวข้อที่เกินจาก 10 ไม่ถือว่าผิด
- ลำดับหัวข้อสลับกันได้ ตรวจแค่ความต่อเนื่องของเลข (ไม่ข้าม ไม่ซ้ำ) และการมีอยู่
  ของหัวข้อบังคับ เลขที่พิมพ์ในเอกสารอาจเพี้ยน อย่ายึดเลขที่พิมพ์เป็นความจริง
- คุณสมบัติที่ไม่เป็นสาระสำคัญให้จัดเป็น severity "ควรปรับปรุง" ไม่ใช่ "ผิดระเบียบ" (ว 214)
- severity มีสองค่าเท่านั้น: "ผิดระเบียบ" (ต้องแก้) หรือ "ควรปรับปรุง" (คำแนะนำ)
- "ค่าที่สกัดได้จากเอกสาร" ที่ให้มาเป็นตัวช่วยกากบาทเช็คตัวเลขอีกชั้น ไม่ใช่คำตอบสุดท้าย
  ถ้าค่าที่สกัดได้ขัดกับสิ่งที่อ่านได้จากเนื้อหา TOR ให้ยึดเนื้อหา และถ้าค่าใดระบุว่า
  "สกัดอัตโนมัติไม่ได้" ไม่ได้แปลว่าไม่มีในเอกสาร ให้อ่านค่านั้นจากเนื้อหาเองตามปกติ
- ทุก finding ต้องมี rule_id ที่อยู่ใน checklist (ระบบจะเติม citation และตัวบทกฎหมายให้เอง
  ตาม rule_id — ห้ามเขียน citation มาเอง)
- ทุก finding ต้องมี evidence = "ยกข้อความตรงคำ" จากเนื้อหา TOR ที่เป็นหลักฐานของข้อผิด
  คัดลอกมาทั้งวรรคโดยไม่ดัดแปลง (ระบบจะตรวจว่าข้อความนี้มีอยู่จริงในเอกสาร ถ้าไม่ตรงจะตัดเฉพาะ
  ช่อง evidence ทิ้ง ไม่ตัดทั้ง finding อย่ากลั้นไม่รายงานเพียงเพราะ quote ตรงคำได้ยาก)
  ถ้าเป็นข้อผิดแบบ "ขาดหัวข้อ/ขาดข้อความ" ที่ไม่มีวรรคให้ยก ให้เว้น evidence เป็นค่าว่าง
- ก่อนรายงานทุก finding ให้ย้อนตรวจกับ "ตัวบทกฎหมายอ้างอิง" ด้านบนอีกครั้งว่าเข้าข่ายจริง
  หากตัวบทมีข้อยกเว้นที่ครอบคลุมกรณีนั้น
  (เช่น ค่าปรับเสริมงาน IT รายชั่วโมงตามข้อ 163 หรือเกณฑ์ราคาอย่างเดียวที่ชอบด้วยมาตรา 65)
  อย่ารายงานว่า "ผิดระเบียบ" — ถ้าเป็นเพียงความไม่เหมาะสมให้ใช้ "ควรปรับปรุง"
- ตรวจรูปแบบจำนวนเงิน (NUM-1): จำนวนเงินควรมีทศนิยมสองตำแหน่ง เช่น 1,000.00 บาท
  ถ้าขาดทศนิยมให้รายงานเป็น "ควรปรับปรุง" ยกเว้นจำนวนเต็มที่ลงท้าย "บาทถ้วน"
- ถ้า TOR ไม่มีปัญหาในกฎข้อใด อย่าสร้าง finding ของกฎข้อนั้น (กันการแจ้งเตือนเกินจริง){fewshot_section}"""


def format_user_message(ctx: CheckContext) -> str:
    """user message: ประเภท/แบบ + ค่าตัวเลขที่ extract มาแล้ว + เนื้อหา TOR ทั้งฉบับ."""
    # regex สกัดไม่ได้ = "ไม่ทราบ" ไม่ใช่ "ไม่มี" — สั่งให้โมเดลอ่านเอง อย่าตีความว่าฟิลด์นั้นว่าง
    # (กัน false negative: regex พลาด → เคยรายงานว่า "ไม่พบ" → ไปกดให้โมเดลไม่ตรวจกฎเชิงตัวเลข)
    unread = "สกัดอัตโนมัติไม่ได้ — อ่านจากเนื้อหาเอง"
    lines = [
        f"ประเภทการจัดซื้อจัดจ้าง: {ctx.procurement_type}",
        f"แบบเอกสาร: {ctx.form}",
        "",
        "ค่าที่สกัดได้จากเอกสาร (ตัวช่วยกากบาทเช็คตัวเลขอีกชั้น ไม่ใช่คำตอบสุดท้าย — "
        "ถ้าขัดกับเนื้อหาให้ยึดเนื้อหา):",
        f"- อัตราค่าปรับที่พบ (%): {ctx.penalty_rate if ctx.penalty_rate is not None else unread}",
        f"- พบค่าปรับหน่วยรายชั่วโมง: {'ใช่' if ctx.has_hourly_penalty else unread}",
        f"- ร้อยละงวดเงินแต่ละงวด: {ctx.payment_percentages if ctx.payment_percentages else unread}",
    ]
    if ctx.project_period:
        lines.append(f"- ระยะเวลาโครงการ: {ctx.project_period[0]} ถึง {ctx.project_period[1]}")
    else:
        lines.append(f"- ระยะเวลาโครงการ: {unread}")
    if ctx.delivery_dates:
        dd = "; ".join(f"{d.label}={d.deadline}" for d in ctx.delivery_dates)
        lines.append(f"- วันครบกำหนดแต่ละงวด: {dd}")
    else:
        lines.append(f"- วันครบกำหนดแต่ละงวด: {unread}")
    lines += ["", "เนื้อหา TOR ทั้งฉบับ:", ctx.raw_text]
    return "\n".join(lines)

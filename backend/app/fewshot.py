def build_fewshot_block() -> str:
    """ประกอบตัวอย่าง few-shot ทั้งหมดเป็นข้อความเดียวสำหรับฝังใน system prompt.

    คืนค่าว่างถ้ายังไม่มีตัวอย่าง (degrade เป็น zero-shot ได้เอง)
    """
    if not EXAMPLES:
        return ""
    blocks = []
    for i, ex in enumerate(EXAMPLES, 1):
        # ตัด citation/provision ออกจากตัวอย่าง — ระบบเติมให้เองตาม rule_id (apply_references)
        # ตัวอย่างจึงสอน "รูปแบบที่โมเดลควรผลิต" ตรง ๆ (มี evidence ไม่มี citation)
        findings_json = json.dumps(
            [f.model_dump(exclude={"citation", "provision"}) for f in ex.findings],
            ensure_ascii=False,
            indent=2,
        )
        note_line = f"\nเหตุผล: {ex.note}" if ex.note else ""
        blocks.append(
            f"### ตัวอย่างที่ {i}: {ex.label}\n"
            f"[ประเภท: {ex.procurement_type} | แบบ: {ex.form}]\n"
            f"เนื้อหา TOR:\n{ex.tor_excerpt}\n\n"
            f"ผลตรวจที่ถูกต้อง (findings):\n{findings_json}{note_line}"
        )
    return "\n\n".join(blocks)

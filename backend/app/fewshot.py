"""ตัวอย่าง few-shot — คู่ (TOR → ผลตรวจที่ถูกต้อง) ทั้งตัวอย่างผิดและตัวอย่างถูก สอนโมเดล (spec §5, architecture.md).

ที่มา: docs/03 ไฟล์ฉบับผิด (A) และฉบับแก้ไข (B) เฉลย (findings) verify จาก "การอ่าน PDF
ต้นฉบับโดยตรง" (ไม่ผ่าน OCR) เทียบ A↔B ทีละมาตรา + อ่านลายมือกรรมการที่แก้ในเอกสาร
  - ตัวอย่าง "ผิด" (A): สอนให้จับข้อผิด
  - ตัวอย่าง "ถูก" (B): findings น้อย/ว่าง — สอนไม่ให้แจ้งเตือนเกิน (anti over-flag)

จุดที่ "ดูเผินเหมือนผิดแต่ถูก" (คุม false-positive):
  - ค่าปรับรายชั่วโมงงาน IT (02A/02B ข้อ 11.2) ชอบด้วยระเบียบ 2560 ข้อ 163 — ไม่ flag เป็นผิด
  - เกณฑ์ราคาอย่างเดียว ชอบด้วยมาตรา 65 จึงไม่ใช่ violation แต่ "แคบ" สำหรับงานซับซ้อน → CRIT-2 ควรปรับปรุง
  - 01B ข้อ 2.9 ตัวแทนจำหน่ายเพียงราย/สิทธิบัตร ติดมาในฉบับถูก = คุณสมบัติเฉพาะที่จำเป็น ไม่ flag

ข้อควรระวังด้านข้อมูล (architecture.md): ตัวอย่างที่ใช้ที่นี่ "ห้ามซ้ำ" กับชุดวัดผล held-out
ปัจจุบันกัน 03A/03B ไว้เป็นชุดทดสอบ จึงไม่นำมาเป็น few-shot

สถานะเฉลย (verify จาก source PDF):
  01A→01B  01A: STRUCT-1 (ขาดวงเงิน/ค่าปรับ — กรรมการเขียนเพิ่มมือ) + STRUCT-2 (เลขข้อ 2.x ซ้ำ/ข้าม)
           + QUAL-1 (2.7 ขาด 'บุคคลธรรมดา') | 01B: ถูก
  02A→02B  02A: STRUCT-2 (เลขข้อ 3↔4 สลับ) + CRIT-2 (เกณฑ์ราคาแคบ) + NUM-1 (เงินไม่มี .00)
           | 02B: เหลือ CRIT-2 ควรปรับปรุง (numbering/เงิน .00 แก้แล้ว ค่าปรับชั่วโมง legit)
  04A      ไม่มีฉบับ B → excerpt ย่อเฉพาะจุดวันที่/งวดเงิน → DATE-1, PAY-2
"""

